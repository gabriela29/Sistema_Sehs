        private void listaempleados(){
        DefaultTableModel tabla = (DefaultTableModel) dtabla.getModel();
        tabla.setRowCount(0);
        try {
            for(EmpresaTO ex : obj.empresaListar()){
                Object[] fila = {ex.getEmpresaid(), ex.getNombre(), ex.getRuc(), ex.getDireccion(), ex.getUrl()};
                tabla.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
            }

    private void consultar() throws Exception {
    //   emp = obj.empresaBuscar();
        if ( emp != null) {
            txtnombre.setText(emp.getNombre());
            txtruc.setText(emp.getRuc());
        } else {
        JOptionPane.showMessageDialog(this, "Empresa no existe");
        }
        
    
    }

     private EmpresaTO datosEmpresa(){
     emp = new EmpresaTO();
     emp.setNombre(txtnombre.getText());
     emp.setRuc(txtruc.getText());
    return emp;        
    }
        
